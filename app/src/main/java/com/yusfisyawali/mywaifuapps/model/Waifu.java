package com.yusfisyawali.mywaifuapps.model;

public class Waifu {
    private int photo;
    private String name;
    private String anime;
    private String birth;
    private String zodiak;
    private String age;
    private String blood;
    private String heightBody;
    private String aboutWaifu;
    private String animeDetail;

    public int getPhoto() {
        return photo;
    }

    public void setPhoto(int photo) {
        this.photo = photo;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getAnime() {
        return anime;
    }

    public String getBirth() {
        return birth;
    }

    public void setBirth(String birth) {
        this.birth = birth;
    }

    public void setAnime(String anime) {
        this.anime = anime;
    }

    public String getZodiak() {
        return zodiak;
    }

    public void setZodiak(String zodiak) {
        this.zodiak = zodiak;
    }

    public String getAge() {
        return age;
    }

    public void setAge(String age) {
        this.age = age;
    }

    public String getBlood() {
        return blood;
    }

    public void setBlood(String blood) {
        this.blood = blood;
    }

    public String getHeightBody() {
        return heightBody;
    }

    public void setHeightBody(String heightBody) {
        this.heightBody = heightBody;
    }

    public String getAboutWaifu() {
        return aboutWaifu;
    }

    public void setAboutWaifu(String aboutWaifu) {
        this.aboutWaifu = aboutWaifu;
    }

    public String getAnimeDetail() {
        return animeDetail;
    }

    public void setAnimeDetail(String animeDetail) {
        this.animeDetail = animeDetail;
    }
}
